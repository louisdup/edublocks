/**
 * Utility functions for the logo.
 */
export class LogoUtilities {
	/**
	 * Returns the full logo path.
	 */
	public static getFullLogoPath(): string {
		return "/images/logo/logo.svg";
	}

	/**
	 * Returns the mini logo path.
	 */
	public static getMiniLogoPath(): string {
		return "/images/logo/logo-mini.svg";
	}
}